    private void assignTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_assignTableMouseClicked
        int selectedRow = assignTable.getSelectedRow();
        if (selectedRow != -1) {
            int actualRow = assignTable.convertRowIndexToModel(selectedRow);
            
            // Get User ID from Column 0
            String userId = model.getValueAt(actualRow, 0).toString(); 
            
            // CALL THE POPULATE METHOD in the parent frame
            parentFrame.populateStudentDetails(userId);
            
            this.dispose(); // Close this popup
        }            // TODO add your handling code here:
    }//GEN-LAST:event_assignTableMouseClicked

