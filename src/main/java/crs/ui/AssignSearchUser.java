    private void assign_search_txtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_assign_search_txtKeyReleased
        String query = assign_search_txt.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        assignTable.setRowSorter(sorter);
        
        if (query.trim().length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }        // TODO add your handling code here:
    }//GEN-LAST:event_assign_search_txtKeyReleased

